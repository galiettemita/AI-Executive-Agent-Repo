import assert from 'node:assert/strict';
import { readFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { describe, it } from 'node:test';
import { fileURLToPath } from 'node:url';

import adapter from '../index.js';

interface Fixture {
  provider: string;
  action: string;
  metrics_count: number;
}

const testDir = dirname(fileURLToPath(import.meta.url));
const readFixture = (): Fixture =>
  JSON.parse(readFileSync(join(testDir, 'fixtures', 'sentiment-success.json'), 'utf8')) as Fixture;

describe('financial-market-analysis integration', () => {
  it('returns deterministic sentiment metrics payload', async () => {
    const result = await adapter.execute(
      { action: 'sentiment', symbols: ['AAPL', 'MSFT'] },
      {} as never
    );
    const expected = readFixture();

    assert.equal(result.status, 'SUCCESS');
    assert.equal(result.data?.provider, expected.provider);
    assert.equal(result.data?.action, expected.action);
    assert.equal(result.data?.metrics?.length, expected.metrics_count);
  });
});
